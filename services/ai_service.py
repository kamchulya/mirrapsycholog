async def chat_psychologist(user_message: str, context: list, user_name: str = "") -> str:
    """Психолог — сократовский диалог"""
    system = SYSTEM_PSYCHOLOGIST
    if user_name:
        system += f"\n\nИмя пользователя: {user_name}. Иногда обращайся по имени."

    messages = context + [{"role": "user", "content": user_message}]

    response = await client.messages.create(
        model=MODEL,
        max_tokens=1000,
        system=system,
        messages=messages
    )
    return response.content[0].text


