async def generate_weekly_report(dialogs: list, user_name: str = "") -> str:
    """Еженедельный отчёт"""
    if not dialogs:
        return "На этой неделе у нас не было диалогов. Давай начнём новую неделю вместе! 💜"

    dialogs_text = "\n\n".join([
        f"[{d['created_at'][:10]}] Режим: {d['mode']}\n"
        f"Пользователь: {d['user_message'][:200]}\n"
        f"Mirra: {d['bot_response'][:200]}"
        for d in dialogs
    ])

    prompt = f"""Диалоги пользователя за неделю:

{dialogs_text}

{"Имя пользователя: " + user_name if user_name else ""}

Составь еженедельный отчёт."""

    response = await client.messages.create(
        model=MODEL,
        max_tokens=2000,
        system=SYSTEM_WEEKLY_REPORT,
        messages=[{"role": "user", "content": prompt}]
    )
    return response.content[0].text


async def detect_emotion(text: str) -> str:
    """Определяем эмоцию в сообщении для дневника"""
    response = await client.messages.create(
        model=MODEL,
        max_tokens=50,
        system="Определи главную эмоцию в тексте одним словом на русском. Только слово, без объяснений.",
        messages=[{"role": "user", "content": text}]
    )
    return response.content[0].text.strip()
